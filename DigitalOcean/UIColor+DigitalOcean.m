#import "UIColor+DigitalOcean.h"

@implementation UIColor (DigitalOcean)

+ (UIColor *)DOBlueColor
{
    return rgb(43.0, 126.0, 190.0);
}

+ (UIColor *)DOGreenColor
{
    return rgb(75.0, 127.0, 46.0);
}

+ (UIColor *)DOGrayColor
{
    return [UIColor lightGrayColor];
}

@end
